#include <iostream>
using namespace std;

int main(){
    int number = 1, x = 1;
    number += x ;
    cout << number << endl ;
    number -= x ;
    cout << number << endl ;
    number *= x ;
    cout << number << endl;
    number /= x ;
    cout << number << endl;
    number %= x ;
    cout << number << endl;
}